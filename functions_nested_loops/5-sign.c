#include "main.h"

/**
 * print_sign - prints the sign of number
 * @n: the sign will be print
 *
 * Return: 1 if great than zero, -1 is less than zero else return 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('n');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('n');
		return (0);
	}
	else
	{
		_putchar('n');
		return (-1);
	}
}
